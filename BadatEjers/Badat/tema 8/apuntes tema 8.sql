/* 
% - "que contenga, termine, o empiece", como el *
_ - "que tenga nosecuantos caracteres"
se usan con el like, que sirve para buscar cosas específicas con expresiones regulares

expresiones regulares:
^ - que empiece (fuera del corchete es que sí tenga eso)
				(dentro del corchete es que no tenga eso)
$ - que termine (fuera del corchete es que sí tenga eso)
				(dentro del corchete es que no tenga eso)
* - "que contenga, termine, o empiece", 0 o más veces
+ - "que contenga, termine, o empiece", 1 o más veces
? - "que contenga, termine, o empiece", 0 o 1
. - una especie de limitador, no sé 
{} - el número que vaya dentro indica las veces que quiere que aparezca

